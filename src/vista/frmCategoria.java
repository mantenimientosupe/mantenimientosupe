    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        oModeloCategoria = new modeloCategoria();
        oModeloCategoria.setId(Integer.parseInt(txtCodigo.getText()));
        oModeloCategoria.setDescripcion(txtNombre.getText());
        oModeloCategoria.setEstado(Integer.parseInt(txtEstado.getText()));
        
        boolean resultado;
                 
        if(operacion.equals("NUEVO")){
           resultado = oAbmCategoria.insertarRegistro(oModeloCategoria);
           if(resultado == true){
               JOptionPane.showMessageDialog(null, "Registro Insertado");
           }
       }else if(operacion.equals("MODIFICAR")){
            resultado = oAbmCategoria.actualizarRegistro(oModeloCategoria);
            if(resultado==true){
               JOptionPane.showMessageDialog(null, "Registro Actualizado"); 
            }
       }
       txtCodigo.setText("");
       txtNombre.setText("");
       txtEstado.setText("");
       contenedor.setSelectedIndex(0);
       grilla.setModel(oAbmCategoria.cargarTabla(""));
    }//GEN-LAST:event_btnGuardarActionPerformed

