    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        operacion = "MODIFICAR";
        contenedor.setSelectedIndex(1);
        
        String codigo = "";
        codigo = grilla.getValueAt(grilla.getSelectedRow(), 0).toString();
        oModeloCategoria = new modeloCategoria();
        oModeloCategoria.setId(Integer.parseInt(codigo));
        
        boolean resultado = oAbmCategoria.cargarRegistro(oModeloCategoria);
        if(resultado == true){
            txtCodigo.setText(String.valueOf(oModeloCategoria.getId()));
            txtNombre.setText(oModeloCategoria.getDescripcion());
            txtEstado.setText(String.valueOf(oModeloCategoria.getEstado()));
        }                
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String texto = txtBuscar.getText();
        String opcion = cbxOpcion.getSelectedItem().toString();
        String condicion = "";
        
        if(opcion.equals("DESCRIPCION")){
            condicion = " where descripcion like '%" + texto + "%'";
        }else{
            condicion = " where id_categoria like '%" + texto + "%'";
        }
        grilla.setModel(oAbmCategoria.cargarTabla(condicion));
        txtBuscar.setText("");
    }//GEN-LAST:event_btnBuscarActionPerformed

