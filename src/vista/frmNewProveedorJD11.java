    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        oModeloProveedor = new modeloProveedor();
        oModeloProveedor.setId(Integer.parseInt(txtCodigo.getText()));
        oModeloProveedor.setRazon_social(txtNombre.getText());
        oModeloProveedor.setDocumento_nro(txtRuc.getText());
        oModeloProveedor.setTelefono(txtTelefono.getText());
        oModeloProveedor.setDireccion(txtDireccion.getText());
        oModeloProveedor.setEstado(Integer.parseInt(txtEstado.getText()));

        boolean resultado;

        resultado = oAbmProveedor.insertarRegistro(oModeloProveedor);
            if(resultado == true){
                JOptionPane.showMessageDialog(null, "Registro Insertado");
            if(Origen.equals("frmProducto")){
                oFrmProducto.cbxProveedor.setModel(oAbmProveedor.cargarCombo());
                this.dispose();
            }
                
            }    
    }//GEN-LAST:event_btnGuardarActionPerformed

